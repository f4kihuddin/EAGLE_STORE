document.addEventListener("DOMContentLoaded", function() {
  const rectangles = [
    { class: ".rectangle-3", value: "5 Diamonds" },
    { class: ".rectangle-4", value: "44 (40+4) Diamonds" },
    { class: ".rectangle-5", value: "19 (17+2) Diamonds" },
    { class: ".rectangle-6", value: "85 (77+8) Diamonds" },
    { class: ".rectangle-7", value: "240 (217+23) Diamonds" },
    { class: ".rectangle-8", value: "170 (154+16) Diamonds" },
    { class: ".rectangle-9", value: "296 (256+40) Diamonds" }
  ];

  rectangles.forEach(rect => {
    document.querySelector(rect.class).addEventListener("click", function() {
      localStorage.setItem("selectedDiamonds", rect.value);
    });
  });

  // Event listener untuk tombol Beli Sekarang (text-wrapper-25)
  document.querySelector(".text-wrapper-25").addEventListener("click", function(event) {
    // Cek apakah diamonds telah dipilih
    var selectedDiamonds = localStorage.getItem("selectedDiamonds");
    if (selectedDiamonds) {
      window.location.href = "konfirmasi.html"; // Redirect ke konfirmasi.html
    } else {
      event.preventDefault();
    }
  });
});





