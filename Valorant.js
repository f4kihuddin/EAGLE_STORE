// Buat Button Disabled
document.addEventListener("DOMContentLoaded", function () {
  const whatsappInput = document.querySelector(".text-wrapper-26");
  const purchaseButton = document.querySelector(".text-wrapper-25");

  // Function to check input field and toggle button state
  function toggleButtonState() {
    if (whatsappInput.value.trim() === "") {
      purchaseButton.classList.add("disabled");
      purchaseButton.style.pointerEvents = "none"; // Disable click
    } else {
      purchaseButton.classList.remove("disabled");
      purchaseButton.style.pointerEvents = "auto"; // Enable click
    }
  }

  // Initial check
  toggleButtonState();

  // Add event listener to input field to check on input change
  whatsappInput.addEventListener("input", toggleButtonState);
});

