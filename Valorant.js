document.addEventListener("DOMContentLoaded", function() {
  // Ambil elemen input nomor WhatsApp
  var inputWhatsapp = document.querySelector('.text-wrapper-26');
  // Ambil elemen rectangle-19
  var rectangleElement = document.querySelector('.rectangle-19');

  // Tambahkan event listener untuk memantau perubahan pada input
  inputWhatsapp.addEventListener('input', function() {
      // Periksa apakah input sudah terisi
      if (inputWhatsapp.value.trim() !== '') {
          // Jika sudah terisi, hapus atribut "style" yang mengatur kursor tidak diizinkan
          rectangleElement.style.cursor = 'auto';
      } else {
          // Jika belum terisi, atur kembali kursor tidak diizinkan
          rectangleElement.style.cursor = 'not-allowed';
      }
  });
});

